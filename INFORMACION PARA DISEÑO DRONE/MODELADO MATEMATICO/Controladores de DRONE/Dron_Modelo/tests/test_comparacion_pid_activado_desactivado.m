% FUNCION LOCAL: EJECUTAR UN CASO
% ================================================================

function resultado = simular_caso_comparacion_roll( ...
    activarPID, ...
    configuracion, ...
    P, ...
    Ccontrol, ...
    modelo ...
)
%SIMULAR_CASO_COMPARACION_ROLL
% Ejecuta una simulacion completa con el PID activado o desactivado.

    dt = ...
        configuracion.dt_s;

    tiempoFinal_s = ...
        configuracion.tiempoFinal_s;

    tiempo_s = ...
        (0:dt:tiempoFinal_s).';

    numeroMuestras = ...
        numel(tiempo_s);

    numeroPasos = ...
        numeroMuestras - 1;

    %% Estado inicial

    x = zeros( ...
        modelo.numeroEstados, ...
        1 ...
    );

    % NED: una coordenada zD negativa representa altura positiva.
    x(modelo.idx.zD) = -1.0;

    %% Comando

    comando.roll = 0;
    comando.pitch = 0;
    comando.yaw = 0;
    comando.throttle = 0.50;
    comando.armado = true;

    %% Memoria del PID

    estadoControl = [];

    %% Crear historiales

    historialEstado = zeros( ...
        numeroMuestras, ...
        modelo.numeroEstados ...
    );

    historialMomentoControlRoll_Nm = zeros( ...
        numeroMuestras, ...
        1 ...
    );

    historialMomentoExternoRoll_Nm = zeros( ...
        numeroMuestras, ...
        1 ...
    );

    historialEmpujeMotores_N = zeros( ...
        numeroMuestras, ...
        P.motores.numero ...
    );

    historialEstado(1, :) = ...
        x.';

    %% Inicializar empuje de hover

    empujeHover_N = ...
        P.cuerpo.masa * P.entorno.g;

    [Uinicial, ~] = mezclador_motores_x( ...
        empujeHover_N, ...
        zeros(3, 1), ...
        P ...
    );

    historialEmpujeMotores_N(1, :) = ...
        Uinicial.empujeMotores_N.';

    %% Simulacion temporal

    for k = 1:numeroPasos

        t = ...
            tiempo_s(k);

        %% Crear exactamente la misma perturbacion

        E = perturbacion_pulso_actitud( ...
            t, ...
            configuracion.ejePerturbacion, ...
            configuracion.momentoPerturbacion_Nm, ...
            configuracion.inicioPerturbacion_s, ...
            configuracion.duracionPerturbacion_s ...
        );

        %% Activar o desactivar la correccion de actitud

        if activarPID

            [ ...
                momentoDeseado_B_Nm, ...
                estadoControl, ...
                ~ ...
            ] = controlador_actitud( ...
                comando, ...
                x, ...
                dt, ...
                Ccontrol, ...
                estadoControl, ...
                modelo ...
            );

        else

            % Los motores siguen encendidos.
            %
            % Solamente se elimina la correccion angular.
            momentoDeseado_B_Nm = zeros(3, 1);

        end

        %% Empuje colectivo

        phi_rad = ...
            x(modelo.idx.phi);

        theta_rad = ...
            x(modelo.idx.theta);

        factorVertical = ...
            cos(phi_rad) * cos(theta_rad);

        % Evita que la compensacion colectiva crezca sin limite
        % cuando el caso sin PID alcanza inclinaciones grandes.
        factorVertical = max( ...
            factorVertical, ...
            0.65 ...
        );

        empujeTotal_N = ...
            P.cuerpo.masa * ...
            P.entorno.g / ...
            factorVertical;

        %% Mezclador

        [U, ~] = mezclador_motores_x( ...
            empujeTotal_N, ...
            momentoDeseado_B_Nm, ...
            P ...
        );

        %% Integracion RK4

        x = paso_rk4( ...
            @modelo_no_lineal_12_estados, ...
            t, ...
            x, ...
            dt, ...
            U, ...
            E, ...
            P, ...
            modelo ...
        );

        %% Guardar datos

        historialEstado(k + 1, :) = ...
            x.';

        historialMomentoControlRoll_Nm(k + 1) = ...
            momentoDeseado_B_Nm(1);

        historialMomentoExternoRoll_Nm(k + 1) = ...
            E.momentoExterno_B_Nm(1);

        historialEmpujeMotores_N(k + 1, :) = ...
            U.empujeMotores_N.';

    end

    %% Procesar resultados

    roll_rad = ...
        historialEstado(:, modelo.idx.phi);

    velocidadRoll_rad_s = ...
        historialEstado(:, modelo.idx.p);

    diferenciaEmpujeMotores_N = ...
        max(historialEmpujeMotores_N, [], 2) - ...
        min(historialEmpujeMotores_N, [], 2);

    resultado.pidActivado = ...
        activarPID;

    resultado.tiempo_s = ...
        tiempo_s;

    resultado.estado = ...
        historialEstado;

    resultado.roll_deg = ...
        rad2deg(roll_rad);

    resultado.velocidadRoll_deg_s = ...
        rad2deg(velocidadRoll_rad_s);

    resultado.momentoControlRoll_Nm = ...
        historialMomentoControlRoll_Nm;

    resultado.momentoExternoRoll_Nm = ...
        historialMomentoExternoRoll_Nm;

    resultado.empujeMotores_N = ...
        historialEmpujeMotores_N;

    resultado.diferenciaEmpujeMotores_N = ...
        diferenciaEmpujeMotores_N;

end


%% ================================================================
% FUNCION LOCAL: MARCAR EL PULSO
% ================================================================

function marcar_pulso_comparacion( ...
    ejeGrafica, ...
    inicioPerturbacion_s, ...
    finPerturbacion_s ...
)
