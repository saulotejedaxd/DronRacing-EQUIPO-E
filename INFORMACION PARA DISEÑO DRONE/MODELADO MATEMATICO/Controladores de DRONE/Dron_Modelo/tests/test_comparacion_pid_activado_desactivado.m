%% TEST_COMPARACION_PID_ACTIVADO_DESACTIVADO
% Compara la respuesta del mismo dron ante la misma perturbacion:
%
%   CASO A:
%       Controlador PID de actitud activado.
%
%   CASO B:
%       Controlador PID de actitud desactivado.
%
% IMPORTANTE:
%
% En el caso sin PID, los motores siguen generando el empuje
% colectivo necesario para hover.
%
% Lo unico que se elimina es el momento correctivo:
%
%       M_PID = [0; 0; 0]
%
% Por tanto, esta prueba permite observar claramente la diferencia
% entre:
%
%   - Un modelo fisico con control realimentado.
%   - El mismo modelo fisico sin estabilizacion de actitud.

clearvars;
clc;
close all;

fprintf('\n');
fprintf('============================================================\n');
fprintf(' COMPARACION: PID ACTIVADO CONTRA PID DESACTIVADO\n');
fprintf('============================================================\n\n');

%% ================================================================
% PREPARAR EL CASO BASE TEORICO
% ================================================================

modelo = definicion_modelo();

P = parametros_fisicos();

R = reglas_competencia();

Dproyecto = requisitos_proyecto(R);

Cdiseno = parametros_diseno();

[P, Cdiseno, caso] = caso_base_teorico( ...
    P, ...
    Cdiseno, ...
    R ...
);

G = configuracion_motores_x( ...
    R, ...
    Dproyecto, ...
    Cdiseno.geometria.distanciaCentroMotor, ...
    Cdiseno.geometria.zMotor_B ...
);

P = aplicar_configuracion_motores(P, G);

Ccontrol = parametros_control_actitud(P);

%% ================================================================
% CONFIGURACION DE LA SIMULACION
% ================================================================

configuracion.dt_s = 0.005;

% La prueba termina antes de que el caso sin PID complete
% una rotacion demasiado grande.
configuracion.tiempoFinal_s = 4.00;

configuracion.ejePerturbacion = "roll";

configuracion.momentoPerturbacion_Nm = 0.08;

configuracion.inicioPerturbacion_s = 2.00;

configuracion.duracionPerturbacion_s = 0.20;

configuracion.finPerturbacion_s = ...
    configuracion.inicioPerturbacion_s + ...
    configuracion.duracionPerturbacion_s;

fprintf('Caso utilizado:\n');
fprintf('  %s\n\n', caso.meta.nombre);

fprintf('Perturbacion aplicada:\n');
fprintf('  Eje:               %s\n', ...
    configuracion.ejePerturbacion);

fprintf('  Magnitud:          %.4f N*m\n', ...
    configuracion.momentoPerturbacion_Nm);

fprintf('  Inicio:            %.3f s\n', ...
    configuracion.inicioPerturbacion_s);

fprintf('  Duracion:          %.3f s\n', ...
    configuracion.duracionPerturbacion_s);

fprintf('  Final:             %.3f s\n\n', ...
    configuracion.finPerturbacion_s);

fprintf('Ejecutando caso A: PID activado...\n');

resultadoConPID = simular_caso_comparacion_roll( ...
    true, ...
    configuracion, ...
    P, ...
    Ccontrol, ...
    modelo ...
);

fprintf('Caso A terminado.\n\n');

fprintf('Ejecutando caso B: PID desactivado...\n');

resultadoSinPID = simular_caso_comparacion_roll( ...
    false, ...
    configuracion, ...
    P, ...
    Ccontrol, ...
    modelo ...
);

fprintf('Caso B terminado.\n\n');

%% ================================================================
% CALCULAR METRICAS DE COMPARACION
% ================================================================

indicesDesdePerturbacion = ...
    resultadoConPID.tiempo_s >= ...
    configuracion.inicioPerturbacion_s;

%% Caso con PID

desviacionMaximaConPID_deg = max( ...
    abs( ...
        resultadoConPID.roll_deg( ...
            indicesDesdePerturbacion ...
        ) ...
    ) ...
);

rollFinalConPID_deg = ...
    resultadoConPID.roll_deg(end);

velocidadFinalConPID_deg_s = ...
    resultadoConPID.velocidadRoll_deg_s(end);

momentoPIDMaximo_Nm = max( ...
    abs(resultadoConPID.momentoControlRoll_Nm) ...
);

diferenciaMotoresConPID_N = ...
    max( ...
        resultadoConPID.diferenciaEmpujeMotores_N ...
    );

%% Caso sin PID

desviacionMaximaSinPID_deg = max( ...
    abs( ...
        resultadoSinPID.roll_deg( ...
            indicesDesdePerturbacion ...
        ) ...
    ) ...
);

rollFinalSinPID_deg = ...
    resultadoSinPID.roll_deg(end);

velocidadFinalSinPID_deg_s = ...
    resultadoSinPID.velocidadRoll_deg_s(end);

diferenciaMotoresSinPID_N = ...
    max( ...
        resultadoSinPID.diferenciaEmpujeMotores_N ...
    );

%% Relacion entre las respuestas

if desviacionMaximaConPID_deg > 0

    relacionDesviacion = ...
        desviacionMaximaSinPID_deg / ...
        desviacionMaximaConPID_deg;

else

    relacionDesviacion = Inf;

end

%% ================================================================
% VERIFICACIONES AUTOMATICAS
% ================================================================

assert( ...
    all(isfinite(resultadoConPID.estado), 'all'), ...
    ['El caso con PID produjo valores NaN o infinitos. ' ...
     'Revisa el controlador o el modelo.'] ...
);

assert( ...
    all(isfinite(resultadoSinPID.estado), 'all'), ...
    ['El caso sin PID produjo valores NaN o infinitos. ' ...
     'Revisa el modelo fisico.'] ...
);

assert( ...
    abs(rollFinalConPID_deg) < 1.0, ...
    ['El caso con PID no regreso suficientemente cerca ' ...
     'de roll igual a cero.'] ...
);

assert( ...
    abs(velocidadFinalConPID_deg_s) < 1.0, ...
    ['El caso con PID termino con una velocidad angular ' ...
     'de roll demasiado grande.'] ...
);

assert( ...
    momentoPIDMaximo_Nm > 0.001, ...
    ['El PID no genero un momento correctivo ' ...
     'apreciable durante la prueba.'] ...
);

assert( ...
    min(resultadoConPID.momentoControlRoll_Nm) < -0.001, ...
    ['El PID no genero un momento negativo para oponerse ' ...
     'a la perturbacion positiva.'] ...
);

assert( ...
    abs(rollFinalSinPID_deg) > 20.0, ...
    ['El caso sin PID no se separo lo suficiente de cero. ' ...
     'Revisa si accidentalmente sigue activo el controlador.'] ...
);

assert( ...
    abs(velocidadFinalSinPID_deg_s) > 10.0, ...
    ['El caso sin PID no conserva una velocidad angular ' ...
     'apreciable despues del impulso externo.'] ...
);

assert( ...
    desviacionMaximaSinPID_deg > ...
        2.0 * desviacionMaximaConPID_deg, ...
    ['La diferencia entre el caso con PID y sin PID no es ' ...
     'suficientemente clara.'] ...
);

assert( ...
    diferenciaMotoresConPID_N > 0.001, ...
    ['Con el PID activado no aparecio una diferencia ' ...
     'apreciable entre los motores.'] ...
);

%% ================================================================
% GUARDAR LA COMPARACION
% ================================================================

comparacion.meta.nombre = ...
    "Comparacion PID activado contra PID desactivado";

comparacion.meta.caso = ...
    caso.meta.nombre;

comparacion.configuracion = ...
    configuracion;

comparacion.ganancias = ...
    Ccontrol;

comparacion.conPID = ...
    resultadoConPID;

