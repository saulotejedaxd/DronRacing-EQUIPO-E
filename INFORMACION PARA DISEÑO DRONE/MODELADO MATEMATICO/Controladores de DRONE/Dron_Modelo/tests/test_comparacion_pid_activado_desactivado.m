comparacion.metricas.desviacionMaximaSinPID_deg = ...
    desviacionMaximaSinPID_deg;

comparacion.metricas.rollFinalConPID_deg = ...
    rollFinalConPID_deg;

comparacion.metricas.rollFinalSinPID_deg = ...
    rollFinalSinPID_deg;

comparacion.metricas.velocidadFinalConPID_deg_s = ...
    velocidadFinalConPID_deg_s;

comparacion.metricas.velocidadFinalSinPID_deg_s = ...
    velocidadFinalSinPID_deg_s;

comparacion.metricas.momentoPIDMaximo_Nm = ...
    momentoPIDMaximo_Nm;

comparacion.metricas.diferenciaMotoresConPID_N = ...
    diferenciaMotoresConPID_N;

comparacion.metricas.diferenciaMotoresSinPID_N = ...
    diferenciaMotoresSinPID_N;

comparacion.metricas.relacionDesviacion = ...
    relacionDesviacion;

%% ================================================================
% MOSTRAR RESULTADOS EN CONSOLA
% ================================================================

