fprintf(' RESULTADOS DE LA COMPARACION\n');
fprintf('============================================================\n\n');

fprintf('GANANCIAS DE ROLL:\n');
fprintf('  Kp:                         %.8f\n', ...
    Ccontrol.roll.Kp);

fprintf('  Ki:                         %.8f\n', ...
    Ccontrol.roll.Ki);

fprintf('  Kd:                         %.8f\n\n', ...
    Ccontrol.roll.Kd);

fprintf('CASO A - PID ACTIVADO:\n');
fprintf('  Desviacion maxima:          %.3f grados\n', ...
    desviacionMaximaConPID_deg);

fprintf('  Roll final:                 %.3f grados\n', ...
    rollFinalConPID_deg);

fprintf('  Velocidad final:            %.3f grados/s\n', ...
    velocidadFinalConPID_deg_s);

fprintf('  Momento PID maximo:         %.4f N*m\n', ...
    momentoPIDMaximo_Nm);

fprintf('  Diferencia maxima motores:  %.4f N\n\n', ...
    diferenciaMotoresConPID_N);

fprintf('CASO B - PID DESACTIVADO:\n');
fprintf('  Desviacion maxima:          %.3f grados\n', ...
    desviacionMaximaSinPID_deg);

fprintf('  Roll final:                 %.3f grados\n', ...
    rollFinalSinPID_deg);

fprintf('  Velocidad final:            %.3f grados/s\n', ...
    velocidadFinalSinPID_deg_s);

fprintf('  Diferencia maxima motores:  %.4f N\n\n', ...
