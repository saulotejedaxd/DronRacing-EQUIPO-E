% Marca el inicio y el final de la perturbacion sin agregarlos
% a las leyendas de las graficas.

    xline( ...
        ejeGrafica, ...
        inicioPerturbacion_s, ...
        '--', ...
        'Inicio', ...
        'LineWidth', ...
        1.0, ...
        'LabelVerticalAlignment', ...
        'bottom', ...
        'HandleVisibility', ...
        'off' ...
    );

    xline( ...
        ejeGrafica, ...
        finPerturbacion_s, ...
        '--', ...
        'Fin', ...
        'LineWidth', ...
        1.0, ...
        'LabelVerticalAlignment', ...
        'bottom', ...
        'HandleVisibility', ...
        'off' ...
    );

end