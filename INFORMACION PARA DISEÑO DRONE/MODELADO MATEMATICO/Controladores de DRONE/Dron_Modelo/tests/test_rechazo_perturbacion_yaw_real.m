if exist( ...
        'graficar_rechazo_perturbacion_yaw', ...
        'file' ...
    ) == 2

    graficar_rechazo_perturbacion_yaw( ...
        resultadosYaw ...
    );

else

    fprintf( ...
        ['No se encontro graficar_rechazo_perturbacion_yaw.m.\n' ...
         'La simulacion real fue ejecutada correctamente.\n\n'] ...
    );

end