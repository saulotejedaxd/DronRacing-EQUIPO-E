    ) == 2

    graficar_rechazo_perturbacion_yaw( ...
        resultadosYaw ...
    );

else

    fprintf( ...
        ['El archivo de graficas de yaw todavia no existe.\n' ...
         'La simulacion y las verificaciones ya fueron ejecutadas.\n\n'] ...
    );

end